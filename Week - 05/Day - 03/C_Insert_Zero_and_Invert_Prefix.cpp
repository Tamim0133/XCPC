using namespace std;
using ll = long long int;

void solve()
{
    int n;
    cin >> n;

    vector<int> arr(n);

    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }

    if (arr[n - 1] == 1)
    {
        cout << "NO" << endl;
        return;
    }

    cout << "YES" << endl;

    vector<int> ans;

    for (int i = n - 1; i > -1; i--)
    {
        if (arr[i] == 0)
        {
            if (i == 0 or arr[i - 1] == 0)
            {
                ans.push_back(0);
            }
            else if (arr[i - 1] == 1)
            {
                int cnt = 0, temp;
                int j = i - 1;
                int nxt;

                while (j > -1 and arr[j] == 1)
                {
                    cnt++;
                    nxt = j;
                    j--;
                }

                temp = cnt;

                while (cnt--)
                {
                    ans.push_back(0);
                }
                ans.push_back(temp);
                i = nxt + 1;
            }
        }
    }

    for (auto i : ans)
        cout << i << " ";
    cout << endl;
}

int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}