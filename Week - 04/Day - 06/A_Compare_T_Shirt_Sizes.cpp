using namespace std;
using ll = long long int;

float value(string s)
{
    if (s[0] == 'L')
        return (3 * s.size());
    else if (s[0] == 'S')
        return (0.5 / s.size());
    else
        return 1;
}

void solve()
{
    string s, t;
    cin >> s >> t;

    reverse(s.begin(), s.end());
    reverse(t.begin(), t.end());

    float x = value(s);
    float y = value(t);

    if (x > y)
        cout << '>' << endl;
    else if (x < y)
        cout << '<' << endl;
    else if (x == y)
        cout << '=' << endl;
}

int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}