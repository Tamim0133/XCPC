using namespace std;
using ll = long long int;

void solve()
{
    int Ax, Ay, Bx, By, Cx, Cy;
    cin >> Ax >> Ay >> Bx >> By >> Cx >> Cy;

    int x1 = Bx - Ax;
    int x2 = Cx - Ax;
    int y1 = By - Ay;
    int y2 = Cy - Ay;
    // cout << x1 << " " << x2 << " " << y1 << " " << y2 << endl;

    int dx = 0, dy = 0;

    if ((x1 > 0 and x2 > 0) or (x1 < 0 and x2 < 0))
        dx = min(abs(x1), abs(x2));
    if ((y1 > 0 and y2 > 0) or (y2 < 0 and y1 < 0))
        dy = min(abs(y1), abs(y2));

    cout << dx + dy + 1 << endl;
}

int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}